-- SQL Script:


-- 1. Create Dimension Tables
CREATE TABLE DIM_CUSTOMER (
    CUSTOMER_KEY        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SOURCE_BRANCH       VARCHAR2(10) NOT NULL,
    SOURCE_CUSTOMER_ID  VARCHAR2(50) NOT NULL,
    FIRST_NAME          VARCHAR2(100),
    LAST_NAME           VARCHAR2(100),
    STREET              VARCHAR2(150),
    HOUSE_NUMBER        VARCHAR2(20),
    ZIP_CODE            VARCHAR2(20),
    CITY                VARCHAR2(100),
    GENDER              VARCHAR2(20),
    DATE_OF_BIRTH       DATE,
    AGE_GROUP           VARCHAR2(30)
);


CREATE TABLE DIM_PRODUCT (
    PRODUCT_KEY         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SOURCE_PRODUCT_ID   VARCHAR2(50) NOT NULL,
    PRODUCT_NAME        VARCHAR2(150),
    UNIT_PRICE          NUMBER(10,2),
    PRODUCT_TYPE        VARCHAR2(100),
    BOTTLE_SIZE_LITERS  NUMBER(5,2),
    PRODUCT_CATEGORY    VARCHAR2(30)
);


CREATE TABLE DIM_BRANCH (
    BRANCH_KEY          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BRANCH_NAME         VARCHAR2(50) NOT NULL,
    GEOGRAPHICAL_REGION VARCHAR2(30) NOT NULL
);


CREATE TABLE DIM_TIME (
    TIME_KEY            NUMBER PRIMARY KEY, -- Format: YYYYMMDD
    CALENDAR_DATE       DATE NOT NULL,
    DAY_OF_WEEK         VARCHAR2(20),
    CALENDAR_WEEK       NUMBER(2),
    CALENDAR_MONTH      NUMBER(2),
    CALENDAR_QUARTER    NUMBER(1),
    CALENDAR_YEAR       NUMBER(4)
);


-- 2. Create Central Fact Table with Constraints
CREATE TABLE FACT_SALES (
    CUSTOMER_KEY        NUMBER NOT NULL,
    PRODUCT_KEY         NUMBER NOT NULL,
    BRANCH_KEY          NUMBER NOT NULL,
    TIME_KEY            NUMBER NOT NULL,
    QUANTITY_LITERS     NUMBER(12,2) NOT NULL,
   
    -- Composite Primary Key Constraints
    CONSTRAINT PK_FACT_SALES PRIMARY KEY (CUSTOMER_KEY, PRODUCT_KEY, BRANCH_KEY, TIME_KEY),
   
    -- Foreign Key Reference Constraints
    CONSTRAINT FK_FACT_CUST FOREIGN KEY (CUSTOMER_KEY) REFERENCES DIM_CUSTOMER(CUSTOMER_KEY),
    CONSTRAINT FK_FACT_PROD FOREIGN KEY (PRODUCT_KEY)  REFERENCES DIM_PRODUCT(PRODUCT_KEY),
    CONSTRAINT FK_FACT_BRAN FOREIGN KEY (BRANCH_KEY)  REFERENCES DIM_BRANCH(BRANCH_KEY),
    CONSTRAINT FK_FACT_TIME FOREIGN KEY (TIME_KEY)    REFERENCES DIM_TIME(TIME_KEY)
);